    [0.163721893491133 0.374805598755832 0.10649506903353 0.0699844479004665],...
    'Color',[1 0 0],...
    'String','1.29x10^{3}',...
    'LineStyle','none',...
    'FontWeight','bold',...
    'FontSize',24,...
    'FitBoxToText','off');

% Create textbox
annotation(figure1,'textbox',...
    [0.163721893491131 0.429237947122862 0.10649506903353 0.0699844479004665],...
    'Color',[0 0 1],...
    'String','6.73x10^{3}',...
    'LineStyle','none',...
    'FontWeight','bold',...
    'FontSize',24,...
    'FitBoxToText','off');

%%%%%%% WARNING NOTE %%%%%%%
