function XRayDECX(Parent1, XMatrix1, XMatrix2, Y1)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Example:
%   CXProductionRate(figure,XRayDEComb{:,2:11},XRayCXComb{:,2:11},JunoAtmosphere2kmFlip{:,2})
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
